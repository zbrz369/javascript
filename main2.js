let szam1=parseInt(prompt=('Kérem adjon meg egy pozitív egész számot'));
let szam2=parseInt(prompt=('Kérem adjon meg egy pozitív egész számot'));
//eredménybe kiírni 
let osszeg=szam1+szam2
let kulonbseg=szam1-szam2
let szorzat=szam1*szam2
let hanyados=szam1/szam2
let maradekosztas=szam1%szam2


